<script>
    window.addEventListener('DOMContentLoaded', (event) => {
        $('#send-email').submit(function(e) {
            e.preventDefault();
            const url = "<?= base_url('send-email') ?>"
            $.ajax({
                type: "POST",
                url: url,
                data: $(this).serialize(),
                dataType: "json",
                cache: false,
                success: function(response) {
                    if (response.status == 'error') {
                        $('#error-email').html(response.error.email)
                        $('#error-subject').html(response.error.subject)
                        $('#error-body').html(response.error.body)
                        $('.csrf').val(response.error.csrf)
                    }
                    if (response.status == 'success') {
                        $('#message').html(
                            `<div class="alert alert-success alert-dismissible fade show" role="alert">
                                ${response.data.message}
                                <button type="button" class="close" data-dismiss="alert" aria-label="Close">
                                    <i class="tio clear"></i>
                                </button>
                            </div>`)
                        $('.csrf').val(response.data.csrf)
                    }
                },
                error: function(xhr, status, errorThrown) {
                    console.log(xhr.responseText)
                    console.log(status)
                    console.log(errorThrown)
                }
            });
            return false;
        });
    });
</script>